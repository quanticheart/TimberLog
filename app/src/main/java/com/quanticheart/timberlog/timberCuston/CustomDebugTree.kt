package com.quanticheart.timberlog.timberCuston

import com.quanticheart.timberlog.Extention.sendThrowable
import timber.log.Timber

/**
 * CrashlyticsReportingTree
 * Apenas um exemplo demonstrando o envio de um Throwable para o
 * Crashlytics utilizando o Timber.
 */
class CustomDebugTree : Timber.DebugTree() {
    override fun log(
        priority: Int, tag: String?, message: String, t: Throwable?
    ) {
        t?.sendThrowable()
        super.log(priority, tag, message, t)
    }
}